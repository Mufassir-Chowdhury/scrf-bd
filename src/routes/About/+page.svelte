<h1 class="h1">
    The Social Policy Research Foundation - Bangladesh
</h1>

<h3 class="my-4 h3">
    Who We Are
</h3>

The Social Policy Research Foundation - Bangladesh (SPRF-BD) is proposed to be established as a non-profit organization dedicated to advancing research, analysis, and advocacy in the field of social policy, exclusively focusing on the unique challenges and opportunities within Bangladesh. This proposal outlines the key components, objectives, and strategies for the establishment and operation of SPRF-BD.

<h3 class="my-4 h3">
    Aim and Objectives
</h3>
<h5 class="h5">Aim:</h5>
To promote evidence-based social policy development and implementation for the betterment of Bangladesh.
<h5 class="h5">Objectives:</h5>
Conduct research on pressing social issues specific to Bangladesh.
Analyze existing social policies in the Bangladeshi context and propose improvements.
Disseminate research findings to Bangladeshi policymakers and the public.
Foster dialogue and collaboration among Bangladeshi stakeholders.
Advocate for equitable and effective social policies tailored to Bangladesh's needs.

<h3 class="my-4 h3">
    Motto
</h3>

"Empowering Bangladesh Through Informed Social Policies."

<h3 class="my-4 h3">
    Mission
</h3>

SPRF-BD's mission is to foster research, analysis, and advocacy that contributes to the formulation and implementation of inclusive, equitable, and effective social policies in Bangladesh. We are dedicated to addressing the unique challenges and opportunities within the country through evidence-based research and collaboration.

<h3 class="my-4 h3">
    Vision
</h3>

Our vision is a Bangladesh where evidence-based social policies are the cornerstone of equitable development, social justice, and prosperity. We aspire to be a leading institution driving positive social change in Bangladesh through research, analysis, and informed advocacy.
